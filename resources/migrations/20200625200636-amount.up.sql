CREATE TABLE amount (
  id int(11) NOT NULL AUTO_INCREMENT,
  recipe int(11) NOT NULL,
  ingr int(11) NOT NULL,
  type int(11) DEFAULT NULL,
  amount float DEFAULT NULL,
  PRIMARY KEY (id,recipe,ingr),
  KEY recipe (recipe),
  KEY type (type),
  KEY ingr (ingr),
  CONSTRAINT amount_ibfk_1 FOREIGN KEY (recipe) REFERENCES recipe (id),
  CONSTRAINT amount_ibfk_2 FOREIGN KEY (type) REFERENCES amount_type (id),
  CONSTRAINT ingr FOREIGN KEY (ingr) REFERENCES ingredients (id)
);
